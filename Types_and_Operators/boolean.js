//Boolean: Represents true or false
console.log(true)
console.log(false)
console.log("\n")

// Comparison Operators
console.log(3 > 5)
console.log(5 < 7)
console.log(6 >= 6)
console.log(9 <= 8)
console.log(2 != 1)
console.log("\n")

// Equality Operators
console.log(2 == 3)
console.log(5 === 5)
console.log("\n")

// The difference between a double equals and a tripple equals, is  that a double equals will perform type conversion

console.log(1 == "1") // true
console.log(1 === "1") // false
console.log("\n")

// Logical Operators
console.log(!false)
console.log(true && true)
console.log(false || true)
console.log("\n")